public class Fitur {
    private double sifatBundar;
    private double konveksitas;
    private double soliditas;

    public void setSifatBundar(double sifatBundar) {
        this.sifatBundar = sifatBundar;
    }

    public void  setKonveksitas(double konveksitas) {
        this.konveksitas = konveksitas;
    }

    public void setSoliditas(double soliditas) {
        this.soliditas = soliditas;
    }

    public double getSifatBundar() {
        return sifatBundar;
    }

    public double getKonveksitas() {
        return konveksitas;
    }

    public double getSoliditas() {
        return soliditas;
    }
}
